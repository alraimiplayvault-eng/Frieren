sessions = {}

class GameSession:
    def __init__(self, channel):
        self.channel = channel
        self.players = {}
        self.scores = {}
        self.open = True
        self.answers = []
        self.event = None
        self.round_questions = []
        self.riddle_active = False
        self.riddle_answers = []
        self.riddle_timeout_task = None

# ==============================
# زر الانضمام
# ==============================

class JoinView(discord.ui.View):

    def __init__(self,session):
        super().__init__(timeout=REGISTRATION_TIME)
        self.session=session

    @discord.ui.button(label="انضمام",style=discord.ButtonStyle.primary)
    async def join(self,interaction:discord.Interaction,button):

        if not self.session.open:
            await interaction.response.send_message("انتهى التسجيل.",ephemeral=True)
            return

        user=interaction.user
        self.session.players[user.id]=user
        self.session.scores.setdefault(user.id,0)

        await interaction.response.send_message("تم تسجيلك.",ephemeral=True)

