async def run_game(session):

    await asyncio.sleep(REGISTRATION_TIME)
    session.open=False

    channel=session.channel

    if len(session.players)==0:
        await channel.send("لا أحد انضم.")
        sessions.pop(channel.id)
        return

    session.round_questions = random.sample(VOCAB_QUESTIONS, QUESTIONS_PER_ROUND)
    for i in range(QUESTIONS_PER_ROUND):

        q = session.round_questions[i]
        session.answers=[a.strip() for a in q["answers"]]

        session.event=asyncio.Event()

        async with channel.typing():
            await asyncio.sleep(2)

        await channel.send(q["question"])

        try:
            await asyncio.wait_for(session.event.wait(),timeout=QUESTION_TIME)
        except:
            await channel.send(random.choice(TIMEOUT_RESPONSES))

        await asyncio.sleep(1)

    await end_game(session)

