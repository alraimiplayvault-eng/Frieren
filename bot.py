class GameView(discord.ui.View):

    @discord.ui.button(label="مفردات", style=discord.ButtonStyle.primary)
    async def vocab(self, interaction: discord.Interaction, button):

        channel = interaction.channel

        if channel.id in sessions:
            await interaction.response.send_message("هناك لعبة تعمل.", ephemeral=True)
            return

        session = GameSession(channel)
        sessions[channel.id] = session

        await interaction.response.send_message(
            random.choice(VOCAB_PICK_RESPONSES) + "\nاضغط انضمام للدخول.",
            view=JoinView(session)
        )

        asyncio.create_task(run_game(session))

    @discord.ui.button(label="أحجيات", style=discord.ButtonStyle.secondary)
    async def riddles(self, interaction: discord.Interaction, button):

        channel = interaction.channel

        if channel.id not in sessions:
            session = GameSession(channel)
            sessions[channel.id] = session
        else:
            session = sessions[channel.id]

        await interaction.response.send_message("حسنًا... لنرَ هذا اللغز.")

        await start_riddle(session)

