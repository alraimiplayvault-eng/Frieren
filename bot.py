@bot.event
async def on_message(message):

    if message.author.bot:
        return

    session=sessions.get(message.channel.id)

    if session and session.answers:

        if message.author.id in session.players:

            if message.content.strip() in session.answers:

                session.scores[message.author.id]+=1

                await message.channel.send(
                    random.choice(CORRECT_RESPONSES).format(
                        name=message.author.display_name
                    )
                )

                if session.event:
                    session.event.set()
# التحقق من إجابة الأحجية
    if session and session.riddle_active:
        user_answer = message.content.strip()
        valid_riddle_answers = [a.strip() for a in session.riddle_answers]

        if user_answer in valid_riddle_answers:
            session.riddle_active = False
            session.riddle_answers = []

            if session.riddle_timeout_task:
                session.riddle_timeout_task.cancel()
                session.riddle_timeout_task = None

            await message.channel.send(
                random.choice(RIDDLE_CORRECT_RESPONSES).format(
                    name=message.author.display_name
                )
            )
            return
    await bot.process_commands(message)

