def build_scoreboard(session):
    sorted_scores = sorted(
        session.scores.items(),
        key=lambda item: item[1],
        reverse=True
    )

    lines = []
    for user_id, score in sorted_scores:
        player_name = session.players[user_id].display_name
        lines.append(f"{player_name} — {score}")

    return "\n".join(lines)

async def end_game(session):
    channel = session.channel

    if not session.scores:
        sessions.pop(channel.id, None)
        return

    sorted_scores = sorted(
        session.scores.items(),
        key=lambda item: item[1],
        reverse=True
    )

    winner_id, winner_score = sorted_scores[0]
    winner_name = session.players[winner_id].display_name

    scoreboard_text = build_scoreboard(session)

    await channel.send(
        "انتهت الجولة...\n\n"
        f"**النتائج:**\n{scoreboard_text}"
    )

    await channel.send(
        random.choice(WINNER_RESPONSES).format(name=winner_name)
    )

    sessions.pop(channel.id, None)

