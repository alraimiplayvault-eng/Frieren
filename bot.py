async def end_riddle_timeout(session):
    await asyncio.sleep(RIDDLE_TIME)

    if not session.riddle_active:
        return

    channel = session.channel
    session.riddle_active = False
    session.riddle_answers = []

    await channel.send(random.choice(RIDDLE_TIMEOUT_RESPONSES))


async def start_riddle(session):
    channel = session.channel

    if session.riddle_active:
        await channel.send("مهلًا... ما زلنا نفكر في اللغز الحالي.")
        return

    riddle = random.choice(RIDDLES)

    session.riddle_active = True
    session.riddle_answers = [a.strip() for a in riddle["answers"]]

    if riddle["mutter"]:
        async with channel.typing():
            await asyncio.sleep(2)
        await channel.send(riddle["mutter"])

    elif random.random() < MUTTER_CHANCE:
        async with channel.typing():
            await asyncio.sleep(2)
        await channel.send(random.choice(RIDDLE_MUMBLE_RESPONSES))

    async with channel.typing():
        await asyncio.sleep(2)

    await channel.send(riddle["question"])

    session.riddle_timeout_task = asyncio.create_task(end_riddle_timeout(session))


